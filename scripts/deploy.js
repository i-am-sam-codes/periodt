const main = async () => {
    const [deployer] = await hre.ethers.getSigners();
    const accountBalance = await deployer.getBalance();
  
    console.log("Deploying contracts with account: ", deployer.address);
    console.log("Account balance: ", accountBalance.toString());
  
    const trackerContractFactory = await hre.ethers.getContractFactory("Tracker");
    const trackerContract = await trackerContractFactory.deploy();
    await trackerContract.deployed();
  
    console.log("Tracker address: ", trackerContract.address);
  };
  
  const runMain = async () => {
    try {
      await main();
      process.exit(0);
    } catch (error) {
      console.log(error);
      process.exit(1);
    }
  };
  
  runMain();